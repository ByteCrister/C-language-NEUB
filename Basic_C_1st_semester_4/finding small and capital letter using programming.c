#include <stdio.h>
int main()
{
    char ch;
    printf("enter a letter  :");
    scanf("%c",&ch);
    if (ch>='A' && ch<='Z')
        printf("Capital letter");

        else if
            (ch>='a' && ch<='z')
            printf("Small letter");
        else printf("You have not entered any letter");
        getch();

}
